// 4.1 Площадь круга
let radiusCircle = 5;
let circleArea = Math.PI * radiusCircle ** 2;
console.log("Circle area:", circleArea.toFixed(2));

// 4.2 Площадь прямоугольника
let length = 10;
let width = 4;
let rectangleArea = length * width;
console.log("Rectangle area:", rectangleArea.toFixed(2));

// 4.3 Объём цилиндра
let radiusCylinder = 3;
let height = 7;
let cylinderVolume = Math.PI * radiusCylinder ** 2 * height;
console.log("Cylinder volume:", cylinderVolume.toFixed(2));
